package pkgif.pkgelse;

/**
 *
 * @author jilpatel
 */
public class IfElse {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int n = 7;
        
        if(n==0){
            System.out.print("number n is Zero");
        }
        
        else if(n%2==0){
            System.out.print("number n is Even");
        }
        
        
        else{
            System.out.print("number n is Odd");
        }
           
        
    }
    
}
